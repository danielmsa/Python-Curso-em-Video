n1 = int(input('Insira um número: '))
n2 = int(input('Insira outro número: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
p = n1 ** n2
di = n1 // n2
rd = n1 % n2
print('A soma é: {0} \n A multiplicação é: {1} \n A divisão é: {2} \n A potência é: {3} \n A divisão inteira é: {4} \n O resto da divisão é:{5}'.format(s, m, d, p, di, rd))
