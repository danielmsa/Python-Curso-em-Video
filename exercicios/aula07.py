n1 = int(input('Insira um número: '))
n2 = int(input('Insira outro número: '))
s = n1 + n2
sub = n1 - n2
m = n1 * n2
d = n1 / n2
p = n1 ** n2
di = n1 // n2
rd = n1 % n2
print('O resultado da soma é: {}!'.format(s))
print('O resultado da subtração é: {}!'.format(sub))
print('O resultado da multiplicação é: {}!'.format(m))
print('O resultado da divisão é: {}!'.format(d))
print('O resultado da potência é: {}!'.format(p))
print('O resultado da divisão inteira é: {}!'.format(di))
print('O resultado do resto da divisão é: {}!'.format(rd))



